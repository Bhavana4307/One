public class NestedIf {
    public static void main(String[] args) {
        String input = "bhavana";
        if (input.equalsIgnoreCase("bhavana")) {
            System.out.println("Yeah, it's me!");
        } else if (input.equalsIgnoreCase("prajan")) {
            System.out.println("Yeah, it's my brother!");
        } else {
            System.out.println("Oops, None!!!");

        }
    }
}
